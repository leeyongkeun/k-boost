/** 전체 주제 목록에서 랜덤으로 N개 카테고리를 뽑고, 각 카테고리에서 1개씩 주제를 선택 */
export function pickRandomTopics(count: number = 8): { category: string; topic: string }[] {
  const shuffled = [...TOPIC_POOL].sort(() => Math.random() - 0.5);
  const selected = shuffled.slice(0, Math.min(count, shuffled.length));

  return selected.map((cat) => {
    const randomTopic = cat.topics[Math.floor(Math.random() * cat.topics.length)];
    return { category: cat.category, topic: randomTopic };
  });
}
