async function main() {
  console.log("\n=== 서울 지하철 전체 역 별칭 등록 ===\n");

  const { data: existing } = await supabase.from("area_aliases").select("alias");
  const existingSet = new Set(existing.map(a => a.alias));

  const entries = Object.entries(STATION_MAP);
  const newOnes = entries.filter(([alias]) => !existingSet.has(alias));
  const dups = entries.length - newOnes.length;

  console.log("전체: " + entries.length + "개 / 신규: " + newOnes.length + "개 / 중복 스킵: " + dups + "개");

  if (newOnes.length === 0) {
    console.log("추가할 역이 없습니다.");
    return;
  }

  // 50개씩 배치 insert
  const batchSize = 50;
  let added = 0;
  for (let i = 0; i < newOnes.length; i += batchSize) {
    const batch = newOnes.slice(i, i + batchSize);
    const rows = batch.map(([alias, district_id]) => ({ alias, district_id }));
    const { error } = await supabase.from("area_aliases").insert(rows);
    if (error) {
      console.error("배치 " + (i / batchSize + 1) + " 실패:", error.message);
      // 개별 등록 시도
      for (const row of rows) {
        const { error: e2 } = await supabase.from("area_aliases").insert(row);
        if (!e2) added++;
      }
    } else {
      added += batch.length;
    }
  }

  const { count } = await supabase.from("area_aliases").select("*", { count: "exact", head: true });
  console.log("\n✅ " + added + "개 역 별칭 추가 완료");
  console.log("현재 총 별칭: " + count + "개");
}

main().catch(console.error);
